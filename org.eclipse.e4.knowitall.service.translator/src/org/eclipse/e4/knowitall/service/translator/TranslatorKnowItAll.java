	@Override
	public String answer(String question) {
		Translate.setKey("29AEA70CEE1293E98960A42A5B3AEF135987624C");
		try {
			
			
			String language = question.substring(question.lastIndexOf(" ")+1);
			String word = question.substring(question.indexOf(" ")+1, question.lastIndexOf(" in"));
//			System.err.println("Word: "+word);
//			System.err.println("Language: "+language);
			return language +" word for "+word+" is "+Translate.execute(word.toLowerCase(), Language.valueOf(language.toUpperCase()));
		} catch (Exception e) {
			e.printStackTrace();
			return "Problem occured while translating";
		}
	}

